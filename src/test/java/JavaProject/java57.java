package JavaProject;

public class java57 {
	//Convert string to char
	public static void main(String args[]){  
		String s="hello";  
		char a=s.charAt(0);  
		char b=s.charAt(1); 
		char c=s.charAt(2); 
		char d=s.charAt(3); 
		char e=s.charAt(4); 
		
		System.out.println("1st character is: "+a);  
		System.out.println("2st character is: "+b);  
		System.out.println("3st character is: "+c);  
		System.out.println("4st character is: "+d);  
		System.out.println("5st character is: "+e);  
		
	}
}
