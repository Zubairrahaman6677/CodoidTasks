package JavaProject;

public class java24 {

	//Write a Java program to print stars using for loop,
	//where the number of stars printed should be equal to the row number?
	public static void main(String[] args) {


		for(int i=0;i<=5;i++) {
			
			for(int j=0;j<=5;j++) {
				
				System.out.print("*");
			}
			System.out.println();
		}
	}

}
